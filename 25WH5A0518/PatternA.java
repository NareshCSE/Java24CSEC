package sathvika_25wh5a0518;

import java.util.*; 
public class PatternA {
	public static void main(String[] args) {
		int i,j,n; 
		  Scanner s=new Scanner(System.in); 
		  System.out.println("25WH5A0518");
		  System.out.print("Enter rows:"); 
		  n=s.nextInt(); 
		  for(i=1;i<=n;i++) 
		  { 
		   for(j=1;j<=i;j++) 
		   { 
		    System.out.print("* "); 
		   } 
		   System.out.println(); 
		  } 
	
	}

}

